package vectores;

public class vector3 {

	public static void main(String[] args)
	{
		String [] nombres=new String[5];
		
		//lenamos las posiciones del vector de tipo String
		nombres[0]="jose";
		nombres[1]="pepito";
		nombres[2]="maxi";
		nombres[3]="enrique";
		nombres[4]="manuel";
		
		for(int i=0;i<5;i++) 
		{
			System.out.println(nombres[i]);

		}


	}

}
